// Primitive Data Types

var name = 'Batman';            // String
var age = 45;                   // Number
var superhero = true;           // Boolean
var isHuman;                    // Undefined
var doesExist = null;           // Null

console.log(typeof name);
console.log(typeof age);
console.log(typeof superhero);
console.log(typeof isHuman);
console.log(typeof doesExist);

// Reference Types

const vehicles = ['Batmobile','Batboat','Batcopter','Batcycle'];
const friends = {
    f1:'Flash',
    f2:'Superman',
    f3:'Catwoman'
}
const date = new Date();

console.log(typeof vehicles);
console.log(typeof friends);
console.log(date);

